export const useI18n = () => {
  const { appLanguage } = useAuth()
  const isFilipino = appLanguage === 'Filipino'
  const isBisaya = appLanguage === 'Bisaya'

  const t = useMemo(() => {
    const dictionary = appLanguage === 'Filipino'
      ? FILIPINO_DICTIONARY
      : appLanguage === 'Bisaya'
        ? BISAYA_DICTIONARY
        : null

    return (text) => {
      if (!dictionary || typeof text !== 'string') return text
      return dictionary[text] || text
    }
  }, [appLanguage])

  return { t, isFilipino, isBisaya }
}
